# PERTENENCIA: Evaluar si una secuencia se encuentra en un objeto
# 'in' -> (x in y) TRUE si la secuencia Y se encuentra en X
# 'not in' -> (x not in y) TRUE si la secuencia Y NO se encunetra en X

text = "Cadena de texto"

text2 = "texto"

print(text2 in text) # True

